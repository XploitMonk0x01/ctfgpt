# Known technique patterns to detect in findings
_TECHNIQUE_PATTERNS = {
    "SQL Injection": ["sql injection", "sqli", "sqlmap", "' or 1=1"],
    "XSS": ["cross-site scripting", "xss", "<script>"],
    "Buffer Overflow": ["buffer overflow", "bof", "stack smashing", "segfault"],
    "Directory Traversal": ["path traversal", "../", "lfi", "rfi"],
    "Password Cracking": ["john", "hashcat", "rockyou", "wordlist", "cracked"],
    "Port Scanning": ["nmap", "open port", "port scan"],
    "Directory Bruteforce": ["gobuster", "dirb", "dirbuster", "ffuf"],
    "Steganography": ["stego", "lsb", "steghide", "binwalk", "hidden data"],
    "RSA Attack": ["rsa", "factorization", "small exponent"],
    "ROP Chain": ["rop", "return oriented", "gadget"],
    "Format String": ["format string", "%p", "%x", "%n"],
    "File Analysis": ["file header", "magic bytes", "exiftool", "strings"],
}


def _extract_techniques(findings: list[dict]) -> list[str]:
    """Detect technique names from finding results."""
    found = set()
    all_text = " ".join(
        f.get("result", "") + " " + f.get("command", "")
        for f in findings
    ).lower()

    for technique, keywords in _TECHNIQUE_PATTERNS.items():
        for kw in keywords:
            if kw.lower() in all_text:
                found.add(technique)
                break

    return sorted(found)
